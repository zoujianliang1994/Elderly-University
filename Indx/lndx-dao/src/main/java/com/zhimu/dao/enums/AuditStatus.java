package com.zhimu.dao.enums;

/**
 * 学校审核
 * 
 * @author wangyong
 * 
 */
public enum AuditStatus {
	/**
	 * 学校审核通过
	 */
	PASS("OK"),
	/**
	 * 学校审核不通过
	 */
	NOT("NOT");
	private String value;

	private AuditStatus(String value) {
		this.value = value;
	}

	@Override
	public String toString() {
		return value;
	}
}
