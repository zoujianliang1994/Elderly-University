package com.zhimu.dao.enums;

/**
 *  附件文件身份（文件标识）
 * @author Weiyunchao
 *
 * 2017年8月9日上午9:54:01
 */
public enum AttachmentFileIdentity {

	/**
	 * 三会一课
	 */
	MEETING("三会一课"),
	/**
	 * 其他
	 */
	OTHER("其他");
	
	
	
	
	private String value;

	private AttachmentFileIdentity(String value) {
		this.value = value;
	}
	
	@Override
	public String toString() {
		return value;
	}
}
