package com.zhimu.dao.entity.cms;

import java.util.Date;

import com.zhimu.commons.constant.FolderConstant;

/**
 * 目录实体
 * 
 * @author zsy
 * 
 */
public class Folder {
	/**
	 * 目录Id
	 */
	private String folderId;

	/**
	 * 父亲Id
	 */
	private String fatherId;

	/**
	 * 英文名称
	 */
	private String ename;

	/**
	 * 目录名称
	 */
	private String name;

	/**
	 * 内容
	 */
	private String content;

	/**
	 * 路径
	 */
	private String path;
	/**
	 * 层级
	 */
	private int level;

	/**
	 * 排序
	 */
	private int sort;

	/**
	 * 状态
	 */
	private FolderConstant.Status status;

	/**
	 * 审核
	 */
	private FolderConstant.Check check;

	/**
	 * 创建时间
	 */
	private Date createTime;
	
	/**
	 * 如果该目录是网站根目录，则此字段代表网站的名字
	 */
	private String title;

	private String  picturesId;  //图片ID
	
	private String   picturesPath;//图片地址
	
	private String type;//目录类型
	
	public String getPicturesId() {
		return picturesId;
	}

	public void setPicturesId(String picturesId) {
		this.picturesId = picturesId;
	}

	public String getPicturesPath() {
		return picturesPath;
	}

	public void setPicturesPath(String picturesPath) {
		this.picturesPath = picturesPath;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getFolderId() {
		return folderId;
	}

	public void setFolderId(String folderId) {
		this.folderId = folderId;
	}

	public String getFatherId() {
		return fatherId;
	}

	public void setFatherId(String fatherId) {
		this.fatherId = fatherId;
	}

	public String getEname() {
		return ename;
	}

	public void setEname(String ename) {
		this.ename = ename;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getSort() {
		return sort;
	}

	public void setSort(int sort) {
		this.sort = sort;
	}


	

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}


	public final FolderConstant.Status getStatus() {
		return status;
	}

	public final void setStatus(FolderConstant.Status status) {
		this.status = status;
	}

	public final FolderConstant.Check getCheck() {
		return check;
	}

	public final void setCheck(FolderConstant.Check check) {
		this.check = check;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}
	
	
	
}
