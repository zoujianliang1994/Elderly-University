package com.zhimu.dao.entity.system;

import java.util.List;

/**
 * 
 * 类名称：组织机构下对应角色
 * 
 * @version 2.0
 */
public class DepartRole {

	private String id;
	private String name;
	private String department_id;

	private List<DepartRole> subDepartRole;
	private boolean hasDepartment = false;
	private String treeurl;
	private String target;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDepartment_id() {
		return department_id;
	}

	public void setDepartment_id(String department_id) {
		this.department_id = department_id;
	}

	public List<DepartRole> getSubDepartRole() {
		return subDepartRole;
	}

	public void setSubDepartRole(List<DepartRole> subDepartRole) {
		this.subDepartRole = subDepartRole;
	}

	public boolean isHasDepartment() {
		return hasDepartment;
	}

	public void setHasDepartment(boolean hasDepartment) {
		this.hasDepartment = hasDepartment;
	}

	public String getTreeurl() {
		return treeurl;
	}

	public void setTreeurl(String treeurl) {
		this.treeurl = treeurl;
	}

	public String getTarget() {
		return target;
	}

	public void setTarget(String target) {
		this.target = target;
	}

}